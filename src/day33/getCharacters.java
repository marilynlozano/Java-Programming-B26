package day33;

public class getCharacters {

    public static void lettersAtoZ(){
        for (char i = 'A'; i <= 'Z'; i++){
            System.out.print(i + " ");
        }
        System.out.println();

    }

    public static void lettersatoz(){
        for (char i = 'a'; i <= 'z'; i++){
            System.out.print(i + " ");
        }
        System.out.println();

    }

    public static void lettersZtoA(){
        for (char i = 'Z'; i >= 'A'; i--){
            System.out.print(i + " ");
        }
        System.out.println();

    }

    public static void lettersztoa(){
        for (char i = 'z'; i >= 'a'; i--){
            System.out.print(i + " ");
        }
        System.out.println();

    }

    public static void numbers0to9(){
        for (char i = '0'; i <= '9'; i++){
            System.out.print(i + " ");
        }
        System.out.println();

    }

}
