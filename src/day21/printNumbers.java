package day21;

public class printNumbers {
    public static void main(String[] args) {
        int num = 2;
        while (num <= 100) {
            System.out.println("even numbers " + num);
            num = num + 2;
        }

        int num2 = 1;
        while (num2 <= 100) {
            System.out.println("odd numbers " + num2);
            num2 = num2 + 2;

        }

    }
}
